class Solution
{
    public:
    //Function to rotate a linked list.
    Node* rotate(Node* head, int k)
    {
        // Your code here
        if(head == NULL or k == 0 or head->next == NULL)
            return head;
        
        Node *curr = head;
        
        while(curr -> next != NULL)         // convert LL to a circular
            curr = curr->next;
        
        curr -> next = head;
        
        
        while(k--)                      // now look for the i+1 node and make it as the head
            curr = curr->next;
        
        head = curr->next;
        curr->next = NULL;           // now break the ith pointer and make it null so it becomes the last node
        
        return head;            // fianlly return the new head;
    }
};
    


